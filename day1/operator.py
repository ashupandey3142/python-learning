# Operators
num1 = 10
num2 = 2

# Arithmetic Operators
print("--------Arithmetic Operators---------")
print("num1 + num2 is: ", num1 + num2)
print("num1 - num2 is: ", num1 - num2)
print("num1 * num2 is: ", num1 * num2)
print("num1 / num2 is: ", num1 / num2)
print("num1 // num2 is: ", num1 // num2) # flour division
print("num1 ** num2 is: ", num1 ** num2)
print("num1 % num2 is: ", num1 % num2)

# Assignment operators
print("--------Assignment operators---------")
a = 4
a += 2
a -= 3
print(a)
a *= 2
a /= 2
print(a)
a //= 2
print(a)
a = 5
a **= 2
print(a)

# Comparison operator
print("--------Comparison operator---------")
x = 8
y = 3
print(x > y)
print(x < y)
print(x != y)
print(x == y)

# Logical operator
print("--------Logical operator---------")
print(x == y and x < y)
print(x == y or x < y)
print(not(False))
print(not(True))
